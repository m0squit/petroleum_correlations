PRESSURE_STANDARD = 1.01325  # barsa
TEMPERATURE_STANDARD = (60 - 32) / 1.8 + 273.15  # K

CONSTANT_GAS = 0.083143  # m3*bars/K/kg-M
CONSTANT_GRAVITY = 0.0000980665  # m2*bars/kg

DENSITY_AIR = 1.2232  # kg/m3
MOLAR_MASS_AIR = DENSITY_AIR * (CONSTANT_GAS * TEMPERATURE_STANDARD / PRESSURE_STANDARD)  # kg/kg-M
